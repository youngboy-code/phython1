# while loop
# this is incrementing
number = 25
while number <= 30:
    print(number)
    number += 1

# decrementing
count = 10
while count >= 1:
    print(count)
    count -= 1

# break and continue statement

x = 100
while x <= 105:
    print(x)
    if x == 103:
        break
    x += 1

y = 34
while y < 40:
    y += 1
    if y == 37:
        continue
    print(y)

# for loop

for num in range(10):
    print(num)

for z in range(70, 80):
    print(z)

for w in range(100, 110, 4):
    print(w)

language = ["python" "kotlin" "java"]
for lang in language:
    print(lang)